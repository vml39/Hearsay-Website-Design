  <h1>FAQs</h1>

  <h1>What song should I choose for my solo and how do I know it’s a good
    choice?</h1>

  <p>Pick a song that highlights your abilities and your range. It’s great to
    sing a song beautifully, but your aim is to impress us. That being said, we
    would like to hear the different ways in which you are able to use your
    voice.  If you can belt a high note and you like how it sounds, we would
    love to hear that. But we want to hear other aspects of your voice as well,
    so it wouldn’t be wise to pick a song that you would belt every note. It
    would be helpful to choose a song we would be familiar with- it’s way more
    memorable for us when you sing a song that we know. Most importantly,
    BE CONFIDENT! If you believe that you sound amazing singing your solo,
    we’ll be able to see that and the audition process will become a lot
    more fun.</p>

  <h1>Can I audition for more than one a cappella group even if the auditions
    are scheduled at the same time?</h1>

  <p>Of course! You can audition for as many groups as you would like. If there
    are any major conflicts, be sure to tell Hearsay and the other groups so
    that you’ll be able to make it to all the auditions. We want each group to
    have a fair chance at seeing your talent.</p>

  <h1>Do I need to have prior singing experience to join an a cappella group?</h1>

  <p>Absolutely not! We have members with all types of backgrounds- from the
    most private shower singers to ones who have performed in front of large
    audiences. As long as you have fun singing in a group, it shouldn’t matter.</p>

  <h1>What is the time commitment for Hearsay A Cappella?</h1>

  <p>Typically, we have 2-hour long rehearsals three times a week. YAY more
    time for bonding and Hearsay love! Each semester, the dates and times are
    based on the member’s schedules and altered if there are any conflicts.
    Depending on the types of performances we have lined up, we may also add
    extra rehearsals here and there.</p>

